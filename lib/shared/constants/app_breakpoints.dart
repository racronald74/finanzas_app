/// Puntos de quiebre utilizados para adaptar la interfaz
/// según el ancho disponible.
///
/// Permiten mantener una única interfaz que se adapta
/// a teléfonos, tablets y escritorio.
class AppBreakpoints {
  AppBreakpoints._();

  /// Ancho máximo considerado como teléfono.
  static const double mobile = 600;

  /// Ancho máximo considerado como tablet.
  static const double tablet = 900;
}
